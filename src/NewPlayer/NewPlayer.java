    private void jbtnChooseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnChooseActionPerformed
        JFileChooser file = new JFileChooser();
        file.setCurrentDirectory(new File(System.getProperty("user.home")));

        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.images", "jpg", "png");
        file.addChoosableFileFilter(filter);
        int result = file.showSaveDialog(null);
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = file.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            jPhoto.setIcon(ResizeImage(path, null));
            ImgPath = path;
        } else {
            System.out.println("No File selected");
        }

    }//GEN-LAST:event_jbtnChooseActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (checkInputs() && ImgPath != null) {
            try {
                Connection con = getConnection();
                PreparedStatement ps = con.prepareStatement("insert into PLAYERS (FIRSTNAME, LASTNAME, CITY_REF, GENDER_REF, BIRTHDAY_DATE, TEAM_REF, PICTURE, CATEGORY_REF, ADDRESS, PHONE)"
                        + "values (?,?,?,?,?,?,?,?,?,?)");

                ps.setString(1, jtxtFirstName.getText());
                ps.setString(2, jtxtLastName.getText());
                ps.setInt(3, ((DropDown) jcbCity.getSelectedItem()).getId());
                ps.setInt(4, ((DropDown) jcbGender.getSelectedItem()).getId());
                java.sql.Date sqldate = new java.sql.Date(jdateBirthday.getDate().getTime());
                ps.setDate(5, sqldate);
                ps.setInt(6, ((DropDown) jcbTeam.getSelectedItem()).getId());
                InputStream img = new FileInputStream(new File(ImgPath));
                ps.setBlob(7, img);
                ps.setInt(8, ((DropDown) jcbGender.getSelectedItem()).getId());
                ps.setString(9, jtxtAddress.getText());
                ps.setString(10, jtxtMobilePhone.getText());

                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Inserted!");
            } catch (HeadlessException | FileNotFoundException | SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "One or more fields are empty!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

