    public boolean checkInputs() {
        if (jtxtFirstName.getText() == null
                || jtxtLastName.getText() == null
                || jcbCity.getSelectedIndex() == -1
                || jcbTeam.getSelectedIndex() == -1
                || jcbGender.getSelectedIndex() == -1
                || jdateBirthday.getDate() == null
                || jtxtMobilePhone.getText() == null
                || jtxtAddress.getText() == null) {
            return false;
        }
        return true;
    }

    public ImageIcon ResizeImage(String ImagePath, byte[] pic) {
        ImageIcon myImage = null;
        if (ImagePath != null) {
            myImage = new ImageIcon(ImagePath);
        } else {
            myImage = new ImageIcon(pic);
        }

        Image img = myImage.getImage();
        Image img2 = img.getScaledInstance(jPhoto.getWidth(), jPhoto.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(img2);
        return image;
    }

