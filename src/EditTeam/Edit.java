    public ImageIcon ResizeImage(String ImagePath, byte[] pic) {
        ImageIcon myImage = null;
        if (ImagePath != null) {
            myImage = new ImageIcon(ImagePath);
        } else {
            myImage = new ImageIcon(pic);
        }

        Image img = myImage.getImage();
        Image img2 = img.getScaledInstance(jChoosedPicture.getWidth(), jChoosedPicture.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(img2);
        return image;
    }

    private ArrayList<EditTeam> getTeamsList() {
        ArrayList<EditTeam> teamList = new ArrayList<>();
        Connection con = getConnection();
        String query = "SELECT T.ID, T.TEAM,T.ESTABLISHMENT, C.CITY, T.ADDRESS, T.MOBILE_PHONE, T.PICTURE  FROM TEAMS T join CITIES C on C.ID = T.CITY_REF";

        Statement st;
        ResultSet rs;

        try {

            st = con.createStatement();
            rs = st.executeQuery(query);
            EditTeam Team;

            while (rs.next()) {
                Team = new EditTeam(rs.getInt("ID"), rs.getString("TEAM"), rs.getString("CITY"), rs.getString("ADDRESS"), rs.getString("MOBILE_PHONE"), rs.getDate("ESTABLISHMENT"), rs.getBytes("PICTURE"));
                teamList.add(Team);
            }

        } catch (SQLException ex) {
            Logger.getLogger(Edit.class.getName()).log(Level.SEVERE, null, ex);
        }

        return teamList;

    }

    private void Show_Teams_In_JTable() {
        ArrayList<EditTeam> list = getTeamsList();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        Object[] row = new Object[6];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getID();
            row[1] = list.get(i).getName();
            row[2] = list.get(i).getCity();
            row[3] = list.get(i).getAddress();
            row[4] = list.get(i).getDateEstablishment();
            row[5] = list.get(i).getMobilePhone();

            model.addRow(row);
        }

    }

