    private void jbtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnUpdateActionPerformed
        String UpdateQuery = null;
        PreparedStatement ps = null;
        Connection con = getConnection();

        if (ImgPath == null) {
            try {
                UpdateQuery = "UPDATE TEAMS SET TEAM = ?, ADDRESS = ?, MOBILE_PHONE = ? WHERE ID = ?";
                ps = con.prepareStatement(UpdateQuery);

                ps.setString(1, jtxtName.getText());
                ps.setString(2, jtaAddress.getText());
                ps.setString(3, jtxtMobilePhone.getText());
                ps.setInt(4, Integer.parseInt(jtxtID.getText()));

                ps.executeUpdate();
                Show_Teams_In_JTable();
                JOptionPane.showMessageDialog(null, "Team Updated");

            } catch (SQLException ex) {
                Logger.getLogger(Edit.class.getName()).log(Level.SEVERE, null, ex);
            }

        } // update With Image
        else {
            try {
                InputStream img = new FileInputStream(new File(ImgPath));

                UpdateQuery = "UPDATE TEAMS SET TEAM = ?, ADDRESS = ?, MOBILE_PHONE = ?, PICTURE = ? WHERE ID = ?";

                ps = con.prepareStatement(UpdateQuery);

                ps.setString(1, jtxtName.getText());
                ps.setString(2, jtaAddress.getText());
                ps.setString(3, jtxtMobilePhone.getText());

                ps.setBlob(4, img);

                ps.setInt(5, Integer.parseInt(jtxtID.getText()));

                ps.executeUpdate();
                Show_Teams_In_JTable();
                JOptionPane.showMessageDialog(null, "Team Updated");

            } catch (HeadlessException | FileNotFoundException | NumberFormatException | SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_jbtnUpdateActionPerformed

    private void jbtnChoosePictureActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnChoosePictureActionPerformed
        JFileChooser file = new JFileChooser();
        file.setCurrentDirectory(new File(System.getProperty("user.home")));

        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.images", "jpg", "png");
        file.addChoosableFileFilter(filter);
        int result = file.showSaveDialog(null);
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = file.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            jChoosedPicture.setIcon(ResizeImage(path, null));
            ImgPath = path;
            // }else{
            //  System.out.println("No File selected");
        }

    }//GEN-LAST:event_jbtnChoosePictureActionPerformed

